UINT _gx_multi_line_text_input_cursor_pos_get(GX_MULTI_LINE_TEXT_INPUT *input, GX_POINT *cursor_pos)
{
GX_TEXT_INPUT_CURSOR *cursor_instance = &input -> gx_multi_line_text_input_cursor_instance;

    *cursor_pos = cursor_instance -> gx_text_input_cursor_pos;

    return GX_SUCCESS;
}

